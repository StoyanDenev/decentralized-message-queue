#!/usr/bin/env bash
# determ-wallet verify-stake-unlock CLI test.
#
# Verifies the OFFLINE trustless verifier for an account's stake-unlock
# eligibility — the `s:`-namespace sibling of verify-dapp-registration (d:).
# The command takes an s:-namespace state-proof JSON (the shape returned by the
# `state_proof` RPC), an UNTRUSTED stake-table-row JSON (the stakes-snapshot /
# stake_bulk_query return shape), an operator-supplied 32-byte state_root, and
# a committee-verified head height, then:
#   (a) re-derives the s:-namespace value_hash from the entry's locked +
#       unlock_height (build_state_leaves byte encoding, src/chain/chain.cpp);
#   (b) re-derives key_bytes = "s:" + domain (ASCII);
#   (c) cross-checks both against the proof;
#   (d) Merkle-verifies the proof against the state_root; and
#   (e) computes eligible = (head_height >= unlock_height) — the exact UNSTAKE
#       gate the chain enforces (Chain::apply rejects when head < unlock_height,
#       S-017 producer/validator parity) —
# all entirely in-process (no RPC, no network, no daemon, no subprocess).
# A node that lies about locked / unlock_height produces a value_hash that
# will NOT verify.
#
# Fixtures are generated by an independent Python reference implementation of
# the SAME documented encodings:
#   value_hash = SHA-256( u64_be(locked) || u64_be(unlock_height) )
#   key_bytes  = b"s:" + domain
#   leaf_hash  = SHA-256(0x00 || u32_be(key_len) || key || value_hash)
#   inner_hash = SHA-256(0x01 || left || right)
#   balanced binary tree, sorted by key, duplicate-last to even each odd level.
# So a VALID fixture carries a genuinely-correct root + sibling path + entry,
# and the wallet's verdict is cross-checked against an independent computation.
#
# Covers (~28 assertions):
#   1.  --help text exists + documents every flag.
#   2.  Global help dispatcher does NOT need to list it (curated subset); but
#       the dispatch resolves the command (smoke via --help exit 0).
#   3.  Missing --proof / --entry / --root / --head-height → exit 1.
#   4.  --root wrong length / non-hex → exit 1.
#   5.  Unknown flag → exit 1.
#   6.  File not found (proof / entry) → exit 1.
#   7.  Malformed proof / entry JSON → exit 1 + malformed_proof / _entry.
#   8.  Entry missing a required field → exit 1.
#   9.  Happy path (single-leaf s: tree) VALID + eligible → exit 0.
#  10.  Happy path (multi-leaf, mixed namespaces) VALID → exit 0.
#  11.  --json happy path: strict schema + valid:true + key/value matches.
#  12.  Wrong --root → exit 2 INVALID.
#  13.  Tampered entry field (locked) → value_hash mismatch → exit 2.
#  14.  Entry for a DIFFERENT domain → key mismatch → exit 2.
#  15.  Still-locked entry (head < unlock_height) → VALID but eligible=false
#       + blocks_until_unlock countdown.
#  16.  UINT64_MAX unlock_height (no deregistration pending) → VALID, never
#       eligible, no blocks_until_unlock field.
#  17.  Mixed-case --root still VALID (normalized).
#
# Cluster-free + deterministic. Run from repo root:
#   bash tools/test_wallet_verify_stake_unlock.sh
set -u
cd "$(dirname "$0")/.."
source tools/common.sh

if [ -z "${DETERM_WALLET:-}" ] || [ ! -x "$DETERM_WALLET" ]; then
    echo "  SKIP: determ-wallet binary not found; build with"
    echo "        cmake --build build --config Release --target determ-wallet"
    exit 0
fi

