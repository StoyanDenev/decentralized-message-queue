pass_count=0; fail_count=0
assert_eq() {
  if [ "$1" = "$2" ]; then echo "  PASS: $3"; pass_count=$((pass_count + 1))
  else echo "  FAIL: $3"; echo "       expected: $2"; echo "       got:      $1"; fail_count=$((fail_count + 1)); fi
}
assert_contains() {
  if echo "$1" | grep -q -- "$2"; then echo "  PASS: $3"; pass_count=$((pass_count + 1))
  else echo "  FAIL: $3"; echo "       missing substring: $2"; fail_count=$((fail_count + 1)); fi
}

# ── Reference fixture generator (independent s: encoding + Merkle) ────────────
# Emits a matched (proof.json, entry.json) pair for a stake-table row leaf
# embedded in a multi-leaf state tree, and prints the computed root on stdout.
# The implementation MUST match build_state_leaves (src/chain/chain.cpp) +
# crypto::merkle.cpp exactly.
#
# args: <proof_out> <entry_out> <domain> <locked> <unlock_height>
gen_fixture() {
  $PY - "$@" <<'PYEOF'
import sys, json, hashlib, struct

(proof_out, entry_out, domain, locked, unlock_height) = sys.argv[1:6]
locked        = int(locked)
unlock_height = int(unlock_height)

def u64(v): return struct.pack(">Q", v)

# s:-namespace value_hash (build_state_leaves byte-for-byte).
def stake_value_hash():
    h = hashlib.sha256()
    h.update(u64(locked))
    h.update(u64(unlock_height))
    return h.digest()

def leaf_hash(key, vh):
    return hashlib.sha256(b"\x00" + struct.pack(">I", len(key)) + key + vh).digest()

def inner_hash(l, r):
    return hashlib.sha256(b"\x01" + l + r).digest()

# Build a small mixed-namespace tree so the s: leaf sits among siblings.
skey = b"s:" + domain.encode()
svh  = stake_value_hash()
pairs = [
    (b"a:" + b"acct_x", bytes.fromhex("11" * 32)),
    (b"d:" + b"app_y",  bytes.fromhex("22" * 32)),
    (skey,              svh),
    (b"r:" + b"node_z", bytes.fromhex("33" * 32)),
]
pairs.sort(key=lambda p: p[0])
leaf_count = len(pairs)
target_index = next(i for i, (k, _) in enumerate(pairs) if k == skey)

row = [leaf_hash(k, v) for (k, v) in pairs]
idx = target_index
proof = []
while len(row) > 1:
    if len(row) % 2 == 1:
        row.append(row[-1])
    sib = idx + 1 if idx % 2 == 0 else idx - 1
    proof.append(row[sib])
    row = [inner_hash(row[i], row[i+1]) for i in range(0, len(row), 2)]
    idx //= 2
root = row[0]

proof_doc = {
    "state_root":   root.hex(),
    "namespace":    "s",
    "key_bytes":    skey.hex(),
    "value_hash":   svh.hex(),
    "target_index": target_index,
    "leaf_count":   leaf_count,
    "proof":        [h.hex() for h in proof],
    "height":       100,
}
entry_doc = {
    "domain":        domain,
    "locked":        locked,
    "unlock_height": unlock_height,
}
with open(proof_out, "w") as f: json.dump(proof_doc, f)
with open(entry_out, "w") as f: json.dump(entry_doc, f)
print(root.hex())
PYEOF
}

# ── Single-leaf s: fixture (root == leaf_hash, empty proof). ──────────────────
gen_single() {
  $PY - "$@" <<'PYEOF'
import sys, json, hashlib, struct
(proof_out, entry_out) = sys.argv[1:3]
domain = "solo.example"
locked = 500000
unlock_height = 80
def u64(v): return struct.pack(">Q", v)
h = hashlib.sha256()
h.update(u64(locked)); h.update(u64(unlock_height))
svh = h.digest()
skey = b"s:" + domain.encode()
root = hashlib.sha256(b"\x00" + struct.pack(">I", len(skey)) + skey + svh).digest()
proof_doc = {
    "state_root": root.hex(), "namespace": "s", "key_bytes": skey.hex(),
    "value_hash": svh.hex(), "target_index": 0, "leaf_count": 1,
    "proof": [], "height": 100,
}
entry_doc = {"domain": domain, "locked": locked, "unlock_height": unlock_height}
with open(proof_out, "w") as f: json.dump(proof_doc, f)
with open(entry_out, "w") as f: json.dump(entry_doc, f)
print(root.hex())
PYEOF
}

